/**
 * Find templates matching a generation context
 * @param {Object} context
 * @returns {RecipeTemplate[]}
 */
export function findMatchingTemplates(context) {
  const { meal, mood, cookTime, profile } = context;

  return RECIPE_TEMPLATES.filter((t) => {
    // Meal type match (or template is flexible)
    if (meal && t.mealType !== meal && t.mealType !== "snack") {
      if (meal === "snack" && t.mealType !== "snack") return false;
      if (meal === "frühstück" && t.mealType !== "frühstück") return false;
    }

    // Mood match (if specified)
    if (mood && mood !== "random" && !t.moods.includes(mood)) return false;

    // Cuisine preference (soft match - prefer but don't exclude)
    // This is handled by scoring, not filtering

    return true;
  });
}
